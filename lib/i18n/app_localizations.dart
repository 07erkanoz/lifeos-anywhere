  /// Returns the localized string for the given [key] and [locale].
  static String get(String key, String locale) {
    if (!_translations.containsKey(locale)) {
      locale = 'en';
    }

    final localeMap = _translations[locale];
    if (localeMap != null && localeMap.containsKey(key)) {
      return localeMap[key]!;
    }

    // Fallback to English
    final enMap = _translations['en'];
    if (enMap != null && enMap.containsKey(key)) {
      return enMap[key]!;
    }

    return key;
  }

  /// Returns a localized string with simple {key} placeholder replacement.
  static String format(String key, String locale, Map<String, String> args) {
    var result = get(key, locale);
    for (final entry in args.entries) {
      result = result.replaceAll('{${entry.key}}', entry.value);
    }
    return result;
  }

  /// Detects the best locale based on the system language.
  static String detectLocale() {
    try {
      final platformLocale = Platform.localeName.split('_')[0];
      if (supportedLocales.contains(platformLocale)) {
        return platformLocale;
      }

      final windowLocale = PlatformDispatcher.instance.locale.languageCode;
      if (supportedLocales.contains(windowLocale)) {
        return windowLocale;
      }
    } catch (_) {}
    return 'en';
  }
